def test(a,b,*args,**kwerg):
    print(a)
    print(b)
    print(args)
    print(kwerg)
#test(11,22,33,44,55,mm=1,nn=2)
a=[11,22,33]
b={'aa':100,'bb':200}
#当列表和元组在当做实参传递的时候，如果前面有一个*表示对其进行解包
#意思是：[111,222,333]----->111,222,333
#当字典当做一个实参进行传递的时候，如果前面有**，表示对其解包
#意思是：{‘aa’:100,'bb'=200}------>aa=100,bb=200
test(11,22,*a,**b)
